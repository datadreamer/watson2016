				<div class="sectionbody">
					<div class="sectiontitle">Issues</div>
					<div class="sectiontext">
						Political decision making is not simply a bullet list of separate issues but rather a complex  interconnected network of systems, all of which affect each other. Watson’s capabilities for parallel processing on a given query allow it to evaluate concerns on a variety of metrics, gauging the most beneficial response across all areas. Watson can advise the best options for any given decision based on it’s impact on the global economy, the environment, education, health care, foreign policy, and civil liberties.<br/>
						<br/>
						<div class="socialmediatitle">
							Investment in the Future
						</div>
						Watson will be able to analyze trends in employment, markets, interest rates, education, poverty, crime, taxes, and policy to assess what actions are most suitable to accelerate investment in the nation’s future.  We believe that investing in the youth will be key to America’s success over the next century, and that significant improvements can be made through federal funding completely by reforming the tax code. Some actions that would make the country once again competitive on a global scale:<br/>
						<ul>
							<li>Single-payer national health care.</li>
							<li>Free university level education.</li>
							<li>Ending homelessness.</li>
							<li>Legalizing and regulating personal recreational drug use.</li>
						</ul>
						In addition to investing in people it is obvious that there is a great need for investment in our country’s infrastructure. For decades nations in Europe and Asia have surpassed the United States in the development of clean energy, public transportation solutions, and communication networks, all while our highways, bridges, and dams go uninspected and deteriorate into dangerous conditions. Rebooting infrastructure projects to a level unseen since the 1930’s would boost our domestic workforce with hundreds of thousands of jobs, improve our environment with new clean energy plants, rebuild the middle class and increase the technology sector in the country.<br/>
						<ul>
							<li>Shift bulk of electrical generation to solar, wind, hydroelectric, and wave farm.</li>
							<li>Review/Repair/Replace/Remove highways, bridges, dams.</li>
							<li>Upgrade and subsidize metropolitan public transit solutions for the next century.</li>
							<li>Build and subsidize metropolitan high-speed communication networks.</li>
						</ul>
						<div class="socialmediatitle">
							Global Community
						</div>
						Watson would be able to recognize the international impacts of domestic policy decisions, and understand that our communication networks, transportation solutions, and economic entanglement have created a global community that we can not deny being a part of. As with any community, what is good for our neighbors is good for us. We have a responsibility to care for the human rights, labor practices, income, safety, and environment of the citizens of nations we do dealings with.<br/>
						<ul>
							<li>Ensure a minimum-wage that meets a reasonable cost of living.</li>
							<li>Ensure fair and safe working conditions.</li>
							<li>Ensure global environmental commons protections.</li>
						</ul>
